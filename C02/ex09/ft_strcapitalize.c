char	*ft_strcapitalize(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
			str[i] -= 32;
		i++;
	}
	i = 0;
	if (str[0] >= 'a' && str[0] <= 'z' && str[0])
		str[0] -= 32;
	while (str[i] != '\0')
	{
		if (str[i] >= 'a' && str[i] <= 'z')
		{
			if ((str[i - 1] >= 32 && str[i - 1] <= 47
				) || (str[i - 1] >= 91 && str[i - 1] <= 96
				) || (str[i] >= 123 && str[i - 1] <= 126))
				str[i] = str[i] - 32;
		}
		i++;
	}
	return (str);
}
