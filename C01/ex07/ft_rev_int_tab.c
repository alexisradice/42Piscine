void	ft_rev_int_tab(int *tab, int size)
{
	int	y;
	int	i;
	int	temp;

	i = 0;
	y = size - 1;
	while (i < size / 2)
	{	
		temp = tab[i];
		tab[i] = tab[y];
		tab[y] = temp;
		y--;
		i++;
	}
}
